public class Sums_for {
    public static void main(String[] args) {
        int sum = 0;



        for (int i = 1; i<=10; i++){
            sum = sum+i;

        }
        System.out.println(sum);


        for (int i = 0; i<5; i++) {
            System.out.println("Yay for Loops are Cool!");
        }




    }
}
